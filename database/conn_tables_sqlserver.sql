-- CRIANDO DIMENSÕES E FATO
CREATE TABLE DW.DIM_CLIENTE (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    FAIXA_ETARIA    VARCHAR(100)    NOT NULL,
    SEXO            VARCHAR(10)     NOT NULL
);
GO

CREATE TABLE DW.DIM_LOJA (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    REGIAO          VARCHAR(100)    NOT NULL,
    UF              VARCHAR(10)     NOT NULL,
    CIDADE          VARCHAR(200)    NOT NULL,
    DESCRICAO_REDE  VARCHAR(200)    NOT NULL,
    DESCRICAO_LOJA  VARCHAR(200)    NOT NULL
);
GO

CREATE TABLE DW.DIM_PRODUTO (
    ID                  INT IDENTITY    NOT NULL PRIMARY KEY,
    CATEGORIA           VARCHAR(50)     NOT NULL,
    ORIGEM              VARCHAR(50)     NOT NULL,
    DESCRICAO_PRODUTO   VARCHAR(100)    NOT NULL
);
GO

CREATE TABLE DW.DIM_TEMPO (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    DT_ANO          INTEGER         NOT NULL,
    DT_SEMESTRE     INTEGER         NOT NULL,
    DT_TRIMESTRE    INTEGER         NOT NULL,
    DT_MES          INTEGER         NOT NULL,
    DT_DIA          INTEGER         NOT NULL,
    DT_COMPLETA     DATE            NOT NULL
);
GO

CREATE TABLE DW.FATO_VENDA (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
	PRODUTO_ID      INTEGER         NOT NULL,
    LOJA_ID         INTEGER         NOT NULL,
    TEMPO_ID        INTEGER         NOT NULL,
    CLIENTE_ID      INTEGER         NOT NULL,
    QTDE            NUMERIC(10,2)   NOT NULL,
    VALOR           NUMERIC(10,2)   NOT NULL,
    FOREIGN KEY     (PRODUTO_ID)    REFERENCES DW.DIM_PRODUTO(ID),
    FOREIGN KEY     (LOJA_ID)       REFERENCES DW.DIM_LOJA(ID),
    FOREIGN KEY     (TEMPO_ID)      REFERENCES DW.DIM_TEMPO(ID),
    FOREIGN KEY     (CLIENTE_ID)    REFERENCES DW.DIM_CLIENTE(ID)
);
GO


