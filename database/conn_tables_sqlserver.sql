-- CRIANDO AMBIENTE DO ERP
CREATE SCHEMA ERP;
GO

-- DROP TABLE ERP.PERFIL
CREATE TABLE ERP.PERFIL (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    PERFIL          VARCHAR(50)     NOT NULL
);
GO

-- DROP TABLE ERP.USUARIO
CREATE TABLE ERP.USUARIO (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    NOME            VARCHAR(100)    NOT NULL,
    IDADE           INTEGER         NOT NULL,
    SEXO            VARCHAR(1)      NOT NULL,
    PERFIL_ID       INTEGER         NOT NULL,
    FOREIGN KEY     (PERFIL_ID)     REFERENCES ERP.PERFIL(ID)
);
GO

-- DROP TABLE ERP.MUNICIPIO
CREATE TABLE ERP.MUNICIPIO (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    REGIAO          VARCHAR(50)     NOT NULL,
    UF              VARCHAR(2)      NOT NULL,
    CIDADE          VARCHAR(100)    NOT NULL
);
GO

-- DROP TABLE ERP.REDE
CREATE TABLE ERP.REDE (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    DESCRICAO       VARCHAR(100)    NOT NULL
);
GO

-- DROP TABLE ERP.LOJA
CREATE TABLE ERP.LOJA (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    DESCRICAO       VARCHAR(100)    NOT NULL,
    REDE_ID         INTEGER         NOT NULL,
    MUNICIPIO_ID    INTEGER         NOT NULL,
    FOREIGN KEY     (REDE_ID)                   REFERENCES ERP.REDE(ID),
    FOREIGN KEY     (MUNICIPIO_ID)     			REFERENCES ERP.MUNICIPIO(ID)
);
GO

-- DROP TABLE ERP.CATEGORIA_PRODUTO
CREATE TABLE ERP.CATEGORIA_PRODUTO (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    ORIGEM          VARCHAR(50)     NOT NULL,
    CATEGORIA       VARCHAR(50)     NOT NULL
);
GO

-- DROP TABLE ERP.PRODUTO
CREATE TABLE ERP.PRODUTO (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    DESCRICAO       VARCHAR(100)    NOT NULL,
    CATEGORIA_ID    INTEGER         NOT NULL,
    FOREIGN KEY     (CATEGORIA_ID)  REFERENCES ERP.CATEGORIA_PRODUTO(ID)
);
GO

-- DROP TABLE ERP.VENDA
CREATE TABLE ERP.VENDA (
    ID              INT IDENTITY    NOT NULL PRIMARY KEY,
    QTDE            NUMERIC(10,2)   NOT NULL,
    VALOR           NUMERIC(10,2)   NOT NULL,
    DATA_VENDA      DATE            NOT NULL,
    PRODUTO_ID      INTEGER         NOT NULL,
    LOJA_ID         INTEGER         NOT NULL,
    USUARIO_ID      INTEGER         NOT NULL,
    FOREIGN KEY     (PRODUTO_ID)    REFERENCES ERP.PRODUTO(ID),
    FOREIGN KEY     (LOJA_ID)       REFERENCES ERP.LOJA(ID),
    FOREIGN KEY     (USUARIO_ID)    REFERENCES ERP.USUARIO(ID)
);
GO



